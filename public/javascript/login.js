// create js to enable user to log in; form should contain email, password; if email and password combination are valid, display dashboard, if not valid display an alert with message
async function loginFormHandler(event) {
    event.preventDefault();
  
    const email = document.querySelector('#email-login').value.trim();
    const password = document.querySelector('#password-login').value.trim();
  
    if (email && password) {
      const response = await fetch('/api/users/login', {
        method: 'post',
        body: JSON.stringify({
          email,
          password
        }),
        headers: { 'Content-Type': 'application/json' }
      });
  
      if (response.ok) {
        document.location.replace('/dashboard');
      } else {
        alert(response.statusText);
      }
    }
  }
// the function above will run when the login form submit button is clicked
  document.querySelector('.login-form').addEventListener('submit', loginFormHandler);